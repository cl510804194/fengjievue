	/**
	 * 构建一个<code>BusinessNoEnum.java</code>
	 * 
	 * @param code
	 * @param bizCategory
	 * @param message
	 */
	private BusinessNoEnum(String code, String bizCategory, String message) {
		this.code = code;
		this.bizCategory = bizCategory;
		this.message = message;
	}
	
	public String getCode() {
		return code;
	}
	
	public String getBizCategory() {
		return bizCategory;
	}
	
	public String getMessage() {
		return message;
	}
	
	/**
	 * 通过枚举<code>code</code>获得枚举
	 * 
	 * @param code
	 * @return LocalCacheEnum
	 */
	public static BusinessNoEnum getByCode(String code) {
		for (BusinessNoEnum _enum : values()) {
			if (_enum.getCode().equals(code)) {
				return _enum;
			}
		}
		return null;
	}
	
	public static BusinessNoEnum getByBizCategory(String category) {
		for (BusinessNoEnum _enum : values()) {
			if (_enum.getBizCategory().equals(category)) {
				return _enum;
			}
		}
		return null;
	}
	
	/**
	 * 获取业务标志码
	 * 
	 * @param _enum
	 * @return
	 */
	public static String getBizIdentity(BusinessNoEnum _enum) {
		return _enum.getBizCategory() + _enum.getCode();
	}
	
	public static boolean validBizIdNo(String bizIdentity, String bizNo) {
		
		if (BusinessNoEnum.getByCode(bizNo) == null) {
			return false;
		}
		
		if (BusinessNoEnum.getByBizCategory(bizIdentity) == null) {
			return false;
		}
		
		if (StringUtil.notEquals(BusinessNoEnum.getByCode(bizNo).getBizCategory(), bizIdentity)) {
			return false;
		}
		
		return true;
	}
