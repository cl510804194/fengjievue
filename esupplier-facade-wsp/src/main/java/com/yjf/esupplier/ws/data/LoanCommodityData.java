package com.yjf.esupplier.ws.data;

import java.io.Serializable;
import java.util.Date;

public class LoanCommodityData implements Serializable {
	
	private static final long serialVersionUID = -9189430395557618125L;
	
	/**
	 * 主键id
	 */
	private int commodityId;
	
	/**
	 * 合同id
	 */
	private String contractId;
	
	/**
	 * 贷款商品类别
	 */
	private String commodityType;
	
	/**
	 * 品牌名称
	 */
	private String commodityBrand;
	
	/**
	 * 商品型号
	 */
	private String commodityModel;
	
	/**
	 * 商品价格
	 */
	private long commodityPrice;
	
	private Date rawAddTime;
	
	private Date rawUpdateTime;
	
	public int getCommodityId() {
		return this.commodityId;
	}
	
	public void setCommodityId(int commodityId) {
		this.commodityId = commodityId;
	}
	
	public String getContractId() {
		return this.contractId;
	}
	
	public void setContractId(String contractId) {
		this.contractId = contractId;
	}
	
	public String getCommodityType() {
		return this.commodityType;
	}
	
	public void setCommodityType(String commodityType) {
		this.commodityType = commodityType;
	}
	
	public String getCommodityBrand() {
		return this.commodityBrand;
	}
	
	public void setCommodityBrand(String commodityBrand) {
		this.commodityBrand = commodityBrand;
	}
	
	public String getCommodityModel() {
		return this.commodityModel;
	}
	
	public void setCommodityModel(String commodityModel) {
		this.commodityModel = commodityModel;
	}
	
	public long getCommodityPrice() {
		return this.commodityPrice;
	}
	
	public void setCommodityPrice(long commodityPrice) {
		this.commodityPrice = commodityPrice;
	}
	
	public Date getRawAddTime() {
		return this.rawAddTime;
	}
	
	public void setRawAddTime(Date rawAddTime) {
		this.rawAddTime = rawAddTime;
	}
	
	public Date getRawUpdateTime() {
		return this.rawUpdateTime;
	}
	
	public void setRawUpdateTime(Date rawUpdateTime) {
		this.rawUpdateTime = rawUpdateTime;
	}
	
	@Override
	public String toString() {
		StringBuilder builder = new StringBuilder();
		builder.append("LoanCommodityData [commodityId=");
		builder.append(commodityId);
		builder.append(", contractId=");
		builder.append(contractId);
		builder.append(", commodityType=");
		builder.append(commodityType);
		builder.append(", commodityBrand=");
		builder.append(commodityBrand);
		builder.append(", commodityModel=");
		builder.append(commodityModel);
		builder.append(", commodityPrice=");
		builder.append(commodityPrice);
		builder.append(", rawAddTime=");
		builder.append(rawAddTime);
		builder.append(", rawUpdateTime=");
		builder.append(rawUpdateTime);
		builder.append("]");
		return builder.toString();
	}
	
}
