package com.yjf.esupplier.ws.bill.order;

import com.yjf.esupplier.ws.order.ProcessOrder;

public class PrintReceiptMealOrder extends ProcessOrder {
	private static final long serialVersionUID = 6810657079737267251L;
	long supplierId;
	String batchNo;
	
	public long getSupplierId() {
		return this.supplierId;
	}
	
	public void setSupplierId(long supplierId) {
		this.supplierId = supplierId;
	}
	
	public String getBatchNo() {
		return this.batchNo;
	}
	
	public void setBatchNo(String batchNo) {
		this.batchNo = batchNo;
	}
	
	@Override
	public String toString() {
		StringBuilder builder = new StringBuilder();
		builder.append("PrintReceiptMealOrder [supplierId=");
		builder.append(supplierId);
		builder.append(", batchNo=");
		builder.append(batchNo);
		builder.append("]");
		return builder.toString();
	}
	
}
