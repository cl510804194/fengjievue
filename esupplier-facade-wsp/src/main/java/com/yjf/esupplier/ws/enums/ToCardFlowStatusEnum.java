package com.yjf.esupplier.ws.enums;

public enum ToCardFlowStatusEnum {
	APPLY("APPLY", "申请中"),
    DENY("DENY", "驳回"),
	CHECK_PASS("CHECK_PASS", "审核通过"),
	PROCESSING("PROCESSING", "处理中"),
	SUCCESS("SUCCESS", "成功"),
	FAIL("FAIL", "失败");
	
	/** 枚举值 */
	private final String code;
	
	/** 枚举描述 */
	private final String message;
	
	/**
	 * 
	 * @param code 枚举值
	 * @param message 枚举描述
	 */
	private ToCardFlowStatusEnum(String code, String message) {
		this.code = code;
		this.message = message;
	}
	
	/**
	 * @return Returns the code.
	 */
	public String getCode() {
		return code;
	}
	
	/**
	 * @return Returns the message.
	 */
	public String getMessage() {
		return message;
	}
	
	/**
	 * @return Returns the code.
	 */
	public String code() {
		return code;
	}
	
	/**
	 * @return Returns the message.
	 */
	public String message() {
		return message;
	}
	
	/**
	 * 通过枚举<code>code</code>获得枚举
	 * 
	 * @param code
	 * @return ToCardFlowStatusEnum
	 */
	public static ToCardFlowStatusEnum getByCode(String code) {
		for (ToCardFlowStatusEnum _enum : values()) {
			if (_enum.getCode().equals(code)) {
				return _enum;
			}
		}
		return null;
	}
	
	/**
	 * 获取全部枚举
	 * 
	 * @return List<ToCardFlowStatusEnum>
	 */
	public static java.util.List<ToCardFlowStatusEnum> getAllEnum() {
		java.util.List<ToCardFlowStatusEnum> list = new java.util.ArrayList<ToCardFlowStatusEnum>(
			values().length);
		for (ToCardFlowStatusEnum _enum : values()) {
			list.add(_enum);
		}
		return list;
	}
	
	/**
	 * 获取全部枚举值
	 * 
	 * @return List<String>
	 */
	public static java.util.List<String> getAllEnumCode() {
		java.util.List<String> list = new java.util.ArrayList<String>(values().length);
		for (ToCardFlowStatusEnum _enum : values()) {
			list.add(_enum.code());
		}
		return list;
	}
	
	/**
	 * 通过code获取msg
	 * @param code 枚举值
	 * @return
	 */
	public static String getMsgByCode(String code) {
		if (code == null) {
			return null;
		}
		ToCardFlowStatusEnum _enum = getByCode(code);
		if (_enum == null) {
			return null;
		}
		return _enum.getMessage();
	}
	
	/**
	 * 获取枚举code
	 * @param _enum
	 * @return
	 */
	public static String getCode(ToCardFlowStatusEnum _enum) {
		if (_enum == null) {
			return null;
		}
		return _enum.getCode();
	}
}
