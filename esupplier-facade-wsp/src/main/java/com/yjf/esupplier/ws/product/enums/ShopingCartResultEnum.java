package com.yjf.esupplier.ws.product.enums;

public enum ShopingCartResultEnum {
	ADD("ADD", "新增"),
	DELETE("DELETE", "删除"),
	UPDTAE("UPDTAE", "更新"),
	CLEAR("CLEAR", "清空");
	
	/** 枚举值 */
	private final String code;
	
	/** 枚举描述 */
	private final String message;
	
	/**
	 * 
	 * @param code 枚举值
	 * @param message 枚举描述
	 */
	private ShopingCartResultEnum(String code, String message) {
		this.code = code;
		this.message = message;
	}
	
	/**
	 * @return Returns the code.
	 */
	public String getCode() {
		return code;
	}
	
	/**
	 * @return Returns the message.
	 */
	public String getMessage() {
		return message;
	}
	
	/**
	 * @return Returns the code.
	 */
	public String code() {
		return code;
	}
	
	/**
	 * @return Returns the message.
	 */
	public String message() {
		return message;
	}
	
	/**
	 * 通过枚举<code>code</code>获得枚举
	 * 
	 * @param code
	 * @return ShopingCartResultEnum
	 */
	public static ShopingCartResultEnum getByCode(String code) {
		for (ShopingCartResultEnum _enum : values()) {
			if (_enum.getCode().equals(code)) {
				return _enum;
			}
		}
		return null;
	}
	
	/**
	 * 获取全部枚举
	 * 
	 * @return List<ShopingCartResultEnum>
	 */
	public static java.util.List<ShopingCartResultEnum> getAllEnum() {
		java.util.List<ShopingCartResultEnum> list = new java.util.ArrayList<ShopingCartResultEnum>(
			values().length);
		for (ShopingCartResultEnum _enum : values()) {
			list.add(_enum);
		}
		return list;
	}
	
	/**
	 * 获取全部枚举值
	 * 
	 * @return List<String>
	 */
	public static java.util.List<String> getAllEnumCode() {
		java.util.List<String> list = new java.util.ArrayList<String>(values().length);
		for (ShopingCartResultEnum _enum : values()) {
			list.add(_enum.code());
		}
		return list;
	}
	
	/**
	 * 通过code获取msg
	 * @param code 枚举值
	 * @return
	 */
	public static String getMsgByCode(String code) {
		if (code == null) {
			return null;
		}
		ShopingCartResultEnum _enum = getByCode(code);
		if (_enum == null) {
			return null;
		}
		return _enum.getMessage();
	}
	
	/**
	 * 获取枚举code
	 * @param _enum
	 * @return
	 */
	public static String getCode(ShopingCartResultEnum _enum) {
		if (_enum == null) {
			return null;
		}
		return _enum.getCode();
	}
}
