package com.yjf.esupplier.ws.storage.info;

import java.io.Serializable;

public class ProductStorageDetailInfo implements Serializable {
	private static final long serialVersionUID = -4297934326781653052L;
	private Long id;
	private long productId;
	private String smallPicPath;
	private String productName;
	private Long storageInOutId;
	private Integer billAmount;
	
	public Long getId() {
		return id;
	}
	
	public void setId(Long id) {
		this.id = id;
	}
	
	public long getProductId() {
		return productId;
	}
	
	public void setProductId(long productId) {
		this.productId = productId;
	}
	
	public String getSmallPicPath() {
		return smallPicPath;
	}
	
	public void setSmallPicPath(String smallPicPath) {
		this.smallPicPath = smallPicPath;
	}
	
	public String getProductName() {
		return productName;
	}
	
	public void setProductName(String productName) {
		this.productName = productName;
	}
	
	public Long getStorageInOutId() {
		return storageInOutId;
	}
	
	public void setStorageInOutId(Long storageInOutId) {
		this.storageInOutId = storageInOutId;
	}
	
	public Integer getBillAmount() {
		return billAmount;
	}
	
	public void setBillAmount(Integer billAmount) {
		this.billAmount = billAmount;
	}
	
	@Override
	public String toString() {
		StringBuilder builder = new StringBuilder();
		builder.append("ProductStorageDetailDO [id=");
		builder.append(id);
		builder.append(", productId=");
		builder.append(productId);
		builder.append(", smallPicPath=");
		builder.append(smallPicPath);
		builder.append(", productName=");
		builder.append(productName);
		builder.append(", storageInOutId=");
		builder.append(storageInOutId);
		builder.append(", billAmount=");
		builder.append(billAmount);
		builder.append("]");
		return builder.toString();
	}
	
}
