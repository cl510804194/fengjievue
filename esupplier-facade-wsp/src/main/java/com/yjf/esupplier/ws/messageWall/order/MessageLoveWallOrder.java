package com.yjf.esupplier.ws.messageWall.order;

import com.yjf.common.service.Order;
import com.yjf.esupplier.ws.order.ValidateOrderBase;

/**
 *
 *
 * @author zhouwei
 *
 */
public class MessageLoveWallOrder extends ValidateOrderBase implements Order {
	
	private static final long serialVersionUID = -3149181844396054662L;

	private long userId;
	
	private String userName;
	
	private String boy;
	
	private String girl;
	
	private String message;
	
	private String image;
	
	public long getUserId() {
		return userId;
	}
	
	public void setUserId(long userId) {
		this.userId = userId;
	}
	
	public String getUserName() {
		return userName;
	}
	
	public void setUserName(String userName) {
		this.userName = userName;
	}
	
	public String getBoy() {
		return boy;
	}
	
	public void setBoy(String boy) {
		this.boy = boy;
	}
	
	public String getGirl() {
		return girl;
	}
	
	public void setGirl(String girl) {
		this.girl = girl;
	}
	
	public String getMessage() {
		return message;
	}
	
	public void setMessage(String message) {
		this.message = message;
	}
	
	public String getImage() {
		return image;
	}
	
	public void setImage(String image) {
		this.image = image;
	}
	
	@Override
	public String toString() {
		StringBuilder builder = new StringBuilder();
		builder.append("MessageLoveWallOrder [userId=");
		builder.append(userId);
		builder.append(", userName=");
		builder.append(userName);
		builder.append(", boy=");
		builder.append(boy);
		builder.append(", girl=");
		builder.append(girl);
		builder.append(", message=");
		builder.append(message);
		builder.append(", image=");
		builder.append(image);
		builder.append("]");
		return builder.toString();
	}

}
